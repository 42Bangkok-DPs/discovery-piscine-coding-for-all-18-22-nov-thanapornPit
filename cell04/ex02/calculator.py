def main():
    first_number = float(input("Give me the first number : "))
    second_number = float(input("Give me to the second number : "))
    print("Thank You!")

    print(f"{first_number} + {second_number} = {first_number + second_number}")
    print(f"{first_number} - {second_number} = {first_number - second_number}")
    print(f"{first_number} / {second_number} = {first_number / second_number}")
    print(f"{first_number} * {second_number} = {first_number * second_number}")

if __name__ == "__main__":
    main()